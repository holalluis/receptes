<h3>BRUNYOLS / thermomix</h3>

<div>INGREDIENTS per a uns 50 brunyols</div>

<ul>
	<li>650 g farina de força
	<li>150 g sucre
	<li>65  g llard de porc (també val mantega però el resultat no és el mateix!)
	<li>35  g llevat premsat
	<li>8   g de sal
	<li>200 ml llet sencera
	<li>10  ml aigua naf
	<li>15  ml anís líquid
	<li>15  g matafaluga, celiandre (pot ser en pols o en gra)
	<li>4   ous
	<li>12  g de pell de llimona
	<li>Oli d'oliva

	<li>Instruccions preparar la massa:
		<ul style=list-style-type:decimal>
			<li>Introduir en el got la llard i el llevat, 30 segons, vel 4, a 37 ºC.
			<li>Afegir la resta d'ingredients, excepte els ous. 
			<li>Amassar. 
			<li>Anar afegint els ous un a un. 
			<li>No incorporar l’altre fins que hagi queden ben barrejat. 6 minuts aprox. 
				fins que la massa quedi lligada i una textura fina i  molt elàstica.
			<li>Tapar amb un drap i deixar reposar en un lloc tebi i sense corrent.
			<li>Esperarem dues o tres hores, i teòricament la massa doblarà el seu volum.
			<li>Escalfar l’oli d'oliva en un cassó, perquè quedi un bon espai entre la superfície i la base del cassó. 
				Encara que a la recepta digui que ha de fumejar, feu-me cas, no espereu a que estigui tan calent perquè quedarien negres per fora.
			<li>Ficar el dit índex al mig de la boleta fins a fer-hi un forat, 
				i alçar el brunyol per a abocar-lo a l'oli. 
				Els girarem una sola vegada, quan comencin a daurar-se per sota, 
				i els traurem quan adquireixin un color daurat fosc.
			<li>Ensucrem mentre siguin calents.
		</ul>
	</li>
</ul>

