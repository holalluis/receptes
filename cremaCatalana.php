<h3>Crema catalana</h3>

<ul>
	<li>1 L llet
	<li>1 canó de canyella
	<li>1 pela llimona
	<li>6-8 rovells d'ou
	<li>200-250 g sucre
	<li>40-50 g midó o maizena

	<li>Instruccions: 
		<ul>
			<li>Bullir llet amb canyella i llimona.
			<li>Refredar.
			<li>Barrejar rovells d'ou, sucre i midó i tirar-hi llet.
			<li>Posar la resta de la llet al foc, i tirar-hi la barreja quan arrenqui el bull.
			<li>Treure del foc, batre i repartir.
			<li>Deixar refredar.
		</ul>
	</li>
</ul>
