<h3>Granissat de llimona</h3>

<ul>
	<li>200 g de sucre
	<li>La pell d'una llimona (només la part groga)
	<li>4 llimones pelades (sense la part blanca ni llavors)
  <li>500 g aigua
  <li>500 g glaçons
</ul>

<h4>Instruccions</h4>
<ol>
  <li>Afegir sucre i pell de llimona al vas i pulveritza <b>30 segons, velocitat 10</b>.
  <li>Incorpora llimones i aigua i tritura <b>30 segons, velocitat 5-10 progresssivament</b>.
  <li>Agrega glaçons i troceja <b>10 segons, velocitat 10</b>.
  <li>Servir immediatament.
</ol>

<h4>Info</h4>
<ul>
  <li>Preparació: 5 min
  <li>Dificultat: baixa
  <li>Racions: 6 vasos (200 ml)
</ul>
