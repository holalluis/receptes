<h3>Escudella i carn d'olla</h3>
<div>Per 4 persones</div>

<ul>
  <li>
    Posar en una olla a bullir:
    <ul>
      <li>Conill de vedella
      <li>Ós pelat de vedella
      <li>Carcassa de pollastre (i si en tens, coll i pedrer)
      <li>Ós d'espinada (de tocino)
      <li>Peu de porc (opcional)
      <li>Col (una meitat o una quarta part)
      <li>Una ceba
      <li>Api (2 troncs de fulla)
      <li>Un porro
      <li>4 pastanagues senceres
      <li>Moniato (opcional)
      <li>Nap (opcional)
      <li>Mongetes del ganxet i/o cigrons (opcional)
      <li>4 patates pelades (posar al final perquè no es desfacin)
      <li>Butifarra negra (si t'hi agrada)
    </ul>
  </li>
  <li>
    Per fer la pilota, barrejar en un plat fondo:
    <ul>
      <li>Carn picada
      <li>Pa sec
      <li>Ou
      <li>All
    </ul>
    Donar-li la forma de pilota, enfarinar i posar dins l'olla.
  </li>
  <li>
    Colar el caldo, i posar-hi arròs i fideus (una tassa d'arròs i 2 de fideus)
    durant uns 15-20 minuts
  </li>
